    def listwidgetfuction(self, item):
        print(item.text())
        self.cur.execute('SELECT Value from Stats WHERE Player = "' + item.text() + '"')
        b = self.cur.fetchone()
        if self.radio_wk.isChecked() == True and self.label_wicketkeeper.text() == "1":
            QMessageBox.warning(MainWindow, "Warning", "Cannot add more than 1 wicketkeeper")
        elif len(self.list_playersused.findItems(item.text(),
                                                 QtCore.Qt.MatchExactly)) == 0 and self.list_playersused.count() < 11:
            if int(self.label_ptsavail.text()) > int(b[0]):
                self.list_playersused.addItem(item.text())
                self.list_playersused.sortItems()
                self.listPopulate()
                self.pointsUsedFunction()
